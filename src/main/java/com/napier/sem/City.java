package com.napier.sem;

/**
 * Represents an employee
 */
public class City
{
    /**
     * City ID
     */
    public Integer id;

    /**
     * City name
     */
    public String name;

    /**
     * Country code belonging to city
     */
    public String countryCode;

    /**
     * District city is located in
     */
    public String district;

    /**
     * City population
     */
    public Integer population;
}